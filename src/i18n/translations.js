export const getTranslation = (lang, key, params = {}) => {
  const text = translations[lang]?.[key] || translations.id[key] || key;

  return text.replace(/\{(\w+)\}/g, (_, param) => {
    if (param === "s") {
      return params.n > 1 ? "s" : "";
    }
    return params[param] ?? "";
  });
};
